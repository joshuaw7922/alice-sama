public class Entity {

    protected int X;
	protected int Y;
	
	public Entity() {
		
	}
	
	public Entity(int X, int Y) {
		this.X = X;
		this.Y = Y;
	}
	
	public int getX() {
		return this.X;
	}
	
	public int getY() {
		return this.Y;
	}
	
	public void setX(int x) {
		this.X = x;
	}
	
	public void setY(int y) {
		this.Y = y;
	}

}
